function calcularAreaRetangulo(altura, largura) {   // função para calcular a area do retanguo definida com os parametros "altura, largura"
    const area = altura * largura;                  // declaramos uma variavel "area" usando const, recebendo o valor  altura x largura  
    return area;                                  
  }
  
  const areaDoRetangulo = calcularAreaRetangulo(10, 5);  // outra variavel "areaDoRetangulo." usando const recebendo o valor (10,5) para calcular 
  console.log(`A área do retângulo é ${areaDoRetangulo}.`); // apenas usando o console.log para imprimir uma mensagem no console

